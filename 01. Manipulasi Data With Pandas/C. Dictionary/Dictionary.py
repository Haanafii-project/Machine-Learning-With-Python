# Dictionary merupakan kumpulan pasangan kunci-nilai yang tidak berurutan. Setiap elemen dalam dictionary memiliki kunci yang digunakan mengakses nilainya. Dictionary ditandai kurung kurawal { }.
my_dictionary = { 'Name': 'Hanafi', 'Usia': 21, 'Alamat': 'Medan' }
print(my_dictionary)

# Mengakses nilai dengan kunci pada dictionary
print(my_dictionary['Name'])

# Mengubah nilai pada dictionary
my_dictionary['Usia'] = 22
print(my_dictionary)